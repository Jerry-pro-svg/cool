public class Control {
    public static void main(String[] args) {
        System.out.println("Numbers in descending order: ");
        for (int i = 10; i >=1; i--) {
            System.out.print(i + " ");
        }
        System.out.println("\n");

        System.out.println("Numbers in ascending order: ");
        int j = 1;
        while (j <= 10) {
            System.out.print(j + " ");
            j++;
        }
    }

    
}
